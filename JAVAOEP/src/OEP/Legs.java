package OEP;

public class Legs {

	private String username;
	private int week;
	private String Squats;
	private String StepUp;
	private String LegPress;
	private String Extension;
	private String LegCurl;
	private String StandingCalf;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getWeek() {
		return week;
	}
	public void setWeek(int week) {
		this.week = week;
	}
	public String getSquats() {
		return Squats;
	}
	public void setSquats(String squats) {
		Squats = squats;
	}
	public String getStepUp() {
		return StepUp;
	}
	public void setStepUp(String stepUp) {
		StepUp = stepUp;
	}
	public String getLegPress() {
		return LegPress;
	}
	public void setLegPress(String legPress) {
		LegPress = legPress;
	}
	public String getExtension() {
		return Extension;
	}
	public void setExtension(String extension) {
		Extension = extension;
	}
	public String getLegCurl() {
		return LegCurl;
	}
	public void setLegCurl(String legCurl) {
		LegCurl = legCurl;
	}
	public String getStandingCalf() {
		return StandingCalf;
	}
	public void setStandingCalf(String standingCalf) {
		StandingCalf = standingCalf;
	}
	
	
}
