package OEP;

public class CST {
	
	private String username;
	private int week;
	private String FlatPress;
	private String InclinePress;
	private String ChestPress;
	private String ChestFly;
	private String HeadPress;
	private String Lateral;
	private String FrontCable;
	private String TricepPush;
	
	
	public String getHeadPress() {
		return HeadPress;
	}
	public void setHeadPress(String headPress) {
		HeadPress = headPress;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getWeek() {
		return week;
	}
	public void setWeek(int week) {
		this.week = week;
	}
	public String getFlatPress() {
		return FlatPress;
	}
	public void setFlatPress(String flatPress) {
		FlatPress = flatPress;
	}
	public String getInclinePress() {
		return InclinePress;
	}
	public void setInclinePress(String inclinePress) {
		InclinePress = inclinePress;
	}
	public String getChestPress() {
		return ChestPress;
	}
	public void setChestPress(String chestPress) {
		ChestPress = chestPress;
	}
	public String getChestFly() {
		return ChestFly;
	}
	public void setChestFly(String chestFly) {
		ChestFly = chestFly;
	}
	public String getLateral() {
		return Lateral;
	}
	public void setLateral(String lateral) {
		Lateral = lateral;
	}
	public String getFrontCable() {
		return FrontCable;
	}
	public void setFrontCable(String frontCable) {
		FrontCable = frontCable;
	}
	public String getTricepPush() {
		return TricepPush;
	}
	public void setTricepPush(String tricepPush) {
		TricepPush = tricepPush;
	}
	
	
}
